/* 4. Your pseudocode algorithm here
* for (each of six lines)
* 		for (each of six times)
* 			write an asterisk
*/
public class NestedLoopBox {

	public static void main(String[] args) {
		
			for (int line = 1; line <= 6; line++) { //number of times to repeat
				for (int i = 1; i <= 6; i++) { // number of times to write
					System.out.print("*"); // write asterisks

				}
				System.out.println(" "); // blank output line

	}

}}
